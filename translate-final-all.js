let totalLinesChanged = 0;
let totalFilesChanged = 0;
let remainingChinese = 0;

for (const filePath of files) {
  if (!fs.existsSync(filePath)) {
    console.log(`SKIP (not found): ${path.relative(root, filePath)}`);
    continue;
  }
  
  const content = fs.readFileSync(filePath, 'utf8');
  const lines = content.split('\n');
  let fileChanged = false;
  let fileLinesChanged = 0;
  
  const newLines = lines.map((line, idx) => {
    if (!hasChinese(line)) return line;
    const translated = translateLine(line);
    if (translated !== line) {
      fileChanged = true;
      fileLinesChanged++;
    }
    if (hasChinese(translated)) {
      remainingChinese++;
    }
    return translated;
  });
  
  if (fileChanged) {
    fs.writeFileSync(filePath, newLines.join('\n'), 'utf8');
    totalLinesChanged += fileLinesChanged;
    totalFilesChanged++;
    console.log(`TRANSLATED: ${path.relative(root, filePath)} (${fileLinesChanged} lines)`);
  } else {
    console.log(`NO CHANGE: ${path.relative(root, filePath)}`);
  }
}


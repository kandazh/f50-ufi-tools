// Sort dictionary keys by length (longest first) for greedy matching
const sortedKeys = Object.keys(dictionary).sort((a, b) => b.length - a.length);

// Check if a line contains Chinese characters
function hasChinese(str) {
  return /[\u4e00-\u9fff]/.test(str);
}

// Translate a line by replacing all Chinese segments using the dictionary
function translateLine(line) {
  if (!hasChinese(line)) return line;
  
  let result = line;
  let changed = true;
  let iterations = 0;
  
  while (changed && iterations < 10) {
    changed = false;
    iterations++;
    for (const key of sortedKeys) {
      if (result.includes(key)) {
        result = result.split(key).join(dictionary[key]);
        changed = true;
      }
    }
  }
  
  return result;
}

